static int oled_i2c_major;
static struct i2c_client *client;
static struct class *oled_class;

/* ---------------- I2C SEND ---------------- */

static void oled_cmd(u8 cmd)
{
	u8 buf[2] = {0x00, cmd};
	int ret = i2c_master_send(client, buf, 2);

	if (ret < 0)
		pr_err("OLED: CMD failed\n");
}

static void oled_data(u8 data)
{
	u8 buf[2] = {0x40, data};
	int ret = i2c_master_send(client, buf, 2);

	if (ret < 0)
		pr_err("OLED: DATA failed\n");
}

/* ---------------- INIT ---------------- */

static void oled_init_display(void)
{
	pr_info("OLED: init display\n");

	oled_cmd(0xAE);
	oled_cmd(0xD5); oled_cmd(0x80);
	oled_cmd(0xA8); oled_cmd(0x1F);
	oled_cmd(0xD3); oled_cmd(0x00);
	oled_cmd(0x40);
	oled_cmd(0x8D); oled_cmd(0x14);
	oled_cmd(0x20); oled_cmd(0x00);
	oled_cmd(0xA1);
	oled_cmd(0xC8);
	oled_cmd(0xDA); oled_cmd(0x02);
	oled_cmd(0x81); oled_cmd(0x8F);
	oled_cmd(0xD9); oled_cmd(0xF1);
	oled_cmd(0xDB); oled_cmd(0x40);
	oled_cmd(0xA4);
	oled_cmd(0xA6);
	oled_cmd(0xAF);
}

/* ---------------- CLEAR ---------------- */

static void oled_clear(void)
{
	int i;

	pr_info("OLED: clear\n");

	oled_cmd(0x21); oled_cmd(0x00); oled_cmd(0x7F);
	oled_cmd(0x22); oled_cmd(0x00); oled_cmd(0x03);

	for (i = 0; i < 512; i++)
		oled_data(0x00);
}

